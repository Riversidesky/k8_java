package com.ruby.java.ch06;

public class Armor {
	private String name;
	private int height;

	// alt + shift + s 후 제너레이트 게터엔 세터
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getHeight() {
		return height;
	}
	public void setHeight(int height) {
		this.height = height;
	}

}
